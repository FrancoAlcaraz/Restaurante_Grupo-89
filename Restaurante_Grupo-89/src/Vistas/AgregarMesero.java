    public void cargarcombo() {
        MeseroData md = new MeseroData();
        List<Mesero> list = md.obtenerMeseros();
        for (int i = 0; i < list.size(); i++) {

            jComboMesero.addItem(list.get(i).getDni());
        }

    }

